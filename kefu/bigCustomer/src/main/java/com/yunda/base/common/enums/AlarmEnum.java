public enum AlarmEnum {
	noperson(1, "无人"), unself(2, "非本人"), allself(3, "本人以及其他人");
	private int num;
	private String name;

	AlarmEnum(int ordinal, String name) {
		this.num = ordinal;
		this.name = name;
	}

	/**
	 * @return the num
	 */
	public int getNum() {
		return num;
	}

	/**
	 * @param num the num to set
	 */
	public void setNum(int num) {
		this.num = num;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
