package com.yunda.base.feiniao.customer.domain;

/**
 * @author beidouxing
 * @create 2019/03/13 9:53
 */
public class CooperatePeerDO {
    //网点编码
    private String branchCode;
    //产品类目
    private String productType;
    //合作同行名称
    private String cooperatePeerName;
    //合作同行比例
    private String cooperateRatio;
    //同行合作价格
    private String cooperatePrice;
    //备注
    private String remark;

    public String getBranchCode() {
        return branchCode;
    }

    public void setBranchCode(String branchCode) {
        this.branchCode = branchCode;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public String getCooperatePeerName() {
        return cooperatePeerName;
    }

    public void setCooperatePeerName(String cooperatePeerName) {
        this.cooperatePeerName = cooperatePeerName;
    }

    public String getCooperateRatio() {
        return cooperateRatio;
    }

    public void setCooperateRatio(String cooperateRatio) {
        this.cooperateRatio = cooperateRatio;
    }

    public String getCooperatePrice() {
        return cooperatePrice;
    }

    public void setCooperatePrice(String cooperatePrice) {
        this.cooperatePrice = cooperatePrice;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    @Override
    public String toString() {
        return "CooperatePeerDO{" +
                "branchCode='" + branchCode + '\'' +
                ", productType='" + productType + '\'' +
                ", cooperatePeerName='" + cooperatePeerName + '\'' +
                ", cooperateRatio='" + cooperateRatio + '\'' +
                ", cooperatePrice='" + cooperatePrice + '\'' +
                ", remark='" + remark + '\'' +
                '}';
    }
}
